// A simple function that takes two numbers as arguments and returns their sum
fn add(x: i32, y: i32) -> i32 {
    x + y
}

// A function that takes a number as an argument and returns its square
fn square(x: i32) -> i32 {
    x * x
}

// A function that takes two numbers as arguments and returns their product
fn multiply(x: i32, y: i32) -> i32 {
    x * y
}

// A function that takes a number as an argument and returns its square root
fn sqrt(x: f64) -> f64 {
    x.sqrt()
}
